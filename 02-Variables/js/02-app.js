// Métodos para los Strings

const stringMethods = "Hola con doble comilla";

// Contar caracteres del string. Length es una proiedad de JS. Podemos crear nuestras propias propiedades

console.log(stringMethods.length);

// includes(). Verifica si un string contiene una palabra o caracter devolviendo un booleano

console.log(stringMethods.includes("Hola"));

// indexOf(). Verifica la posición en que se encuentre una palabra dentro del string

console.log(stringMethods.indexOf("Hola"));